#include "libft.h"
#include <string.h>

void				ft_bzero(void *s, size_t n)
{
	char			*dst;
	unsigned int	i;

	dst = (char *)s;
	i = 0;
	while (i < n)
	{
		dst[i] = 0;
		i++;
	}
}
